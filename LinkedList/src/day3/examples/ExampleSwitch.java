package day3.examples;

public class ExampleSwitch {

	
	public static void main (String[] args)
	
	{
		int i = 10;
		
		switch(i){
		case 1:
		System.out.println("case 1");
		break;
		case 2:
		System.out.println("case 2");
		break;
		case 10:
		System.out.println("case 10");
		break;
	}
}
}

